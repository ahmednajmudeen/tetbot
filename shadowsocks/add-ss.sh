echo -e "" | tee -a /etc/log-create-user.log
echo -e "IP/Host : $IP" | tee -a /etc/log-create-user.log
echo -e "Port OBFS TLS : $tls" | tee -a /etc/log-create-user.log
echo -e "Port OBFS HTTP : $http" | tee -a /etc/log-create-user.log
echo -e "Password : $user" | tee -a /etc/log-create-user.log
echo -e "Method : aes-256-cfb" | tee -a /etc/log-create-user.log
echo -e "Expired On : $exp" | tee -a /etc/log-create-user.log
echo -e "" | tee -a /etc/log-create-user.log
echo -e "\033[0;34m━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\033[0m" | tee -a /etc/log-create-user.log
echo -e "Link OBFS TLS : $linkss1" | tee -a /etc/log-create-user.log
echo -e "\033[0;34m━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\033[0m" | tee -a /etc/log-create-user.log
echo -e "Link OBFS HTTP : $linkss2" | tee -a /etc/log-create-user.log
echo -e "\033[0;34m━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\033[0m" | tee -a /etc/log-create-user.log
echo "" | tee -a /etc/log-create-user.log
read -n 1 -s -r -p "Press any key to back on menu"            
ss-menu